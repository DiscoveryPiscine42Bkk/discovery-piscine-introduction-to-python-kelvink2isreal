#!/usr/bin/env python3
first_num = int(input("Give me the first number: "))
sec_num = int(input("Give me the second number: "))
print("Thank You!")
print(f"{first_num} + {sec_num} = {first_num+sec_num}" )
print(f"{first_num} - {sec_num} = {first_num-sec_num}" )
print(f"{first_num} / {sec_num} = {first_num/sec_num}" )
print(f"{first_num} x {sec_num} = {first_num*sec_num}" )